/*
    OPERATOR        MEANING
    =           Assignment Operator
    ==          Comparision Operator (compare if values are equal)
    ===         Strict Equality Operator (compare if values & datatype are equal)
    !=          Inequality operator
    !==         Strict inequality operator
*/

const PI = 3.14

// Using of "==" operator
if(PI == "3.14"){
    console.log("That is PI");
}
else{
    console.log("This is not PI");
}
