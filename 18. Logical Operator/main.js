/*
Logical Operators: Used to combine or manipulate boolean values (true or false)

AND = &&
OR = ||
NOT = !
*/

const temp = 20;

if(temp > 0 && temp <= 30){
    console.log("The weather is GOOD");
}
else{
    console.log("The weather is BAD");
}

const temp1 = 20;

if(temp1 > 0 || temp1 <= 30){
    console.log("The weather is GOOD");
}
else{
    console.log("The weather is BAD");
}